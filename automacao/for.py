#Aprendendo sobre loops com o FOR
amigos = ['Paulo', "Pedro", 'Roberto', 'Tiao', 'Tonho']

for letra in 'google' :
    print(letra)

for amigos in amigos :
    print(amigos)

#imprimir numeros em um range de 10
for numeros in range(10) :
    print(numeros)

print()

for numeros in range(2,20) :
    print(numeros)